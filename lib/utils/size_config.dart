import 'package:flutter/widgets.dart';

class SizeConfig {
  static MediaQueryData? _mediaQueryData;
  static double screenWidth = 0;
  static double screenHeight = 0;
  static double blockSizeHorizontal = 0;
  static double blockSizeVertical = 0;
  static double block2xW = 0;
  static double block2xH = 0;

  static double _safeAreaHorizontal = 0;
  static double _safeAreaVertical = 0;
  static double safeBlockHorizontal = 0;
  static double safeBlockVertical = 0;

  const SizeConfig();

  static void init(BuildContext context) {
    _mediaQueryData = MediaQuery.of(context);
    screenWidth =
        _mediaQueryData!.size.width > 420 ? 420 : _mediaQueryData!.size.width;
    screenHeight =
        _mediaQueryData!.size.height > 898 ? 899 : _mediaQueryData!.size.height;
    print("$screenHeight x $screenWidth");
    blockSizeHorizontal = screenWidth / 100;
    blockSizeVertical = screenHeight / 100;

    block2xW = blockSizeHorizontal * 2;
    block2xH = blockSizeVertical * 2;

    _safeAreaHorizontal =
        _mediaQueryData!.padding.left + _mediaQueryData!.padding.right;
    _safeAreaVertical =
        _mediaQueryData!.padding.top + _mediaQueryData!.padding.bottom;
    // 17 -> 4
    safeBlockHorizontal = (screenWidth - _safeAreaHorizontal) / 100;
    safeBlockVertical = (screenHeight - _safeAreaVertical) / 100;
  }
}
